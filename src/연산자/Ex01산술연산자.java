package 연산자;

public class Ex01산술연산자 {

	public static void main(String[] args) {
		
		// 산술연산자
		// +,*,-,/(몫,)%(나머지)
		
		int num1 = 2;
		int num2 = 5;
		float num3 = 3.14f;
		String num4 = "7";
		
		System.out.println(num1+num2); //7
		System.out.println(num2/num1); //2
		System.out.println(num2%num1); //1
		
		//문자와 숫자를 더하면 연결의 의미가 됨!
		//문자+숫자 = 문자 자료형
		System.out.println(num1+num4); //문자열 자료형 "27"
		
		// 복사 : ctrl +alt+ 아래화살표
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
